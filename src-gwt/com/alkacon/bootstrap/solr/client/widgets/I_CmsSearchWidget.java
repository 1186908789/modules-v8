package com.alkacon.bootstrap.solr.client.widgets;

import com.alkacon.bootstrap.solr.client.CmsSolrDocumentList;

/**
 * Marker interface, used for all widgets thet should be used in the left input panel of the search ui.<p>
 */
public interface I_CmsSearchWidget {

    /**
     * Updates the widget based on the current search result.<p>
     * 
     * @param result the current search result
     */
    void update(CmsSolrDocumentList result);

    /**
     * Signals whether this widget should be updated after a search or not.<p>
     * 
     * @return <code>true</code> if this widget should be updated after search execution
     */
    boolean updateOnSearch();
}
